"""Generate visualization_callbacks.py from DATASET_REGISTRY.yaml."""

from __future__ import annotations

import logging
from pathlib import Path
from textwrap import dedent
from typing import Any

logger = logging.getLogger(__name__)


class CallbackGenerator:
    """Generates Dash callbacks from registry."""

    def __init__(self, registry: dict[str, Any]):
        """Initialize callback generator.

        Args:
            registry: Parsed DATASET_REGISTRY.yaml
        """
        self.registry = registry
        self.datasets = registry.get("datasets", {})

    def generate(self) -> str:
        """Generate visualization_callbacks.py.

        Returns:
            Path to generated file
        """
        output_path = Path(self.registry["generators"]["output_paths"]["callbacks"])
        output_path.parent.mkdir(parents=True, exist_ok=True)

        code = self._generate_module_header()
        code += self._generate_callback_factory_class()
        code += self._generate_dataset_callbacks()

        with open(output_path, "w") as f:
            f.write(code)

        logger.info(f"Generated callbacks module: {output_path}")
        return str(output_path)

