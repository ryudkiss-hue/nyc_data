    def _generate_callback_factory_class(self) -> str:
        """Generate CallbackFactory methods."""
        return ""  # Included in header

    def _generate_dataset_callbacks(self) -> str:
        """Generate callback functions for datasets."""
        code = "\n# ============================================================================\n"
        code += "# Dataset-Specific Callbacks\n"
        code += "# ============================================================================\n\n"

        active_datasets = {
            k: v
            for k, v in self.datasets.items()
            if v.get("status") == "active"
        }

        for key, dataset in sorted(active_datasets.items()):
            code += self._generate_dataset_callback(key, dataset)
            code += "\n"

        return code

